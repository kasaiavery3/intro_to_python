# Parameters and Arguments
# Single Parameter
def greeting(name):
  print('Wsg', name)

greeting('Sai')
greeting('Jane')
greeting('Sharlene')

# Multiple Parameters
def violence(person, area, item):
  print('I love to laugh while hitting', person, 'in the', area, 'with a', item)

violence('my sibling', 'ankle', 'razor scooter')

